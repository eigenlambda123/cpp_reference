#include <iostream>
using namespace std;

int main() {
    int num = 65;       // integer
    char ch = 'A';      // character
    double pi = 3.1415; // floating point
    bool flag = true;   // boolean

    cout << "int: " << num << endl;
    cout << "char: " << ch << endl;
    cout << "double: " << pi << endl;
    cout << "bool: " << flag << endl;
}
